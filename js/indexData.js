//--------------------数字动画---------------
function NumbersInit(){
    var _this=this;
    this.isPhone=utils.isPhone();
    this.H=$(window).height();
    this.h=$('.dataDisplay').height();
    this.t=$('.CandidateArea').offset().top;
    this.flag=false;
    this.init=function(){
        $('.number_box').empty().html(_this.createNumber());
        _this.initNumber();
        // $('.number_box').empty().html(_this.createNumber());
        this.event();
    }
    this.event=function(){
        $(window).on('scroll',function(){
            var stop=$(window).scrollTop();
            if(this.isPhone){
                if(stop>=_this.t){
                    if(!_this.flag){
                        return false;
                    }
                    $('.number_box').find('.number').css({top:0});
                    _this.flag=false;
                    return false;
                }
                if(stop>_this.h+20&&stop<_this.t+50){
                    if(!_this.flag){
                        _this.initNumber();
                        _this.flag=true
                    }
                }
            }else{
                if(stop>=_this.h+_this.H||stop<_this.h){
                    if(!_this.flag){
                        return false;
                    }
                    $('.number_box').find('.number').css({top:0});
                    _this.flag=false;
                    return false;
                }
                if(stop>_this.h&&stop<_this.t){
                    if(!_this.flag){
                        _this.initNumber();
                        _this.flag=true
                    }
                }
            }




        })
    }
    //获取模拟领证人数
    this.getNumber = function(){
        var today = new Date();
        var timerToday = today.getTime();//获取现在 距离 标准时间 多少毫秒
        var thatDay = new Date(2017, 9, 5); //设置月份当前月份要减1
        var timerThatDay = thatDay.getTime();//初始日期 标准时间 多少毫秒
        var dayNum = ( timerToday - timerThatDay) / 1000 / 3600 / 24;
        return Math.ceil(dayNum)*3+5123;
    }
    //创建数字html
    this.createNumber = function(){
        var htm='<span class="number">';
        for(var i=0;i<10;i++){
            htm+='<em>'+ i+'</em>';
        }
        htm+='</span>';
        return htm;
    }
    //启动动画
    this.initNumber=function(){
        _this._numberAnimate(724,'.server1');
        _this._numberAnimate(_this.getNumber(),'.collage_number');
        _this._numberAnimate(358,'.expert_num');
        _this._numberAnimate(23780,'.customers');
    }
    //动画函数
    this._numberAnimate=function (number,className){
        var h=$('.number_box .number').height()/10;
        var array=number.toString().split('');
        for(var i=0;i<array.length;i++){
            var num=Number(array[i])*h;
            $(className).find('.number_box').eq(i).find('.number').animate({top:-num},1000);
        }
    }
}

